											<p>Concordia St. Paul’s Coding Bootcamp is an intensive, fast-paced program where you learn to be a software developer. In this full-time, 12-week course, you will learn either .NET or Java and acquire the skills needed for entry-level developer positions. The course, which includes face-to face lectures, hands-on experience and labs, enables you to build a strong portfolio to aid in your job search. Coding bootcamp provides the perfect bridge for those looking to change or accelerate their career. You will learn to think like a professional coder and write effective, efficient code. Classes are taught by veteran coders who know what businesses need. Small classes ensure lots of personal attention from instructors and mentors. The hands-on knowledge you receive can be immediately applied to a job, making you a valuable employee from the start.</p>
