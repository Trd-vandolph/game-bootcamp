												<h2>Why Coding Bootcamp?</h2>
												<p>The programming industry is a fast growing, high paying field. According to the Bureau of Labor Statistics the job outlook for software developer is expected to grow 22 percent by 2020. Not only is the industry growing at a fast pace, 33% of IT workers in the baby boomer generation are expected to retire in upcoming years.&nbsp; For entry-level programmers in Minnesota, starting salaries average approximately $50,000. Once you have gained three to five years of experience, you will have the opportunity to earn upwards on average approximately $94,000, according to the 2012 BLS Statistics. Concordia University’s Coding Bootcamp teaches the skills Fortune 500 companies desire for an entry-level programmer. If you are a career changer, career builder or wanting to turn your hobby into a career, this intensive course will immerse you in the world of programming and allow you to start a new career quickly. In three months, you will be able to begin your job hunt armed with in-demand knowledge and ability to apply your skills. Coding bootcamp teaches either .NET or Java, two of the most popular coding languages in the world. According to one survey, .NET and Java are among the most frequently used languages for websites. It is not just how many sites use the languages, but also what kinds of sites. Java is one of the most popular languages for high-traffic sites such as LinkedIn and eBay, according to this report. You’ll learn a language that is immediately applicable to many employers, opening up your employment opportunities and making your skill set more appealing.</p>
